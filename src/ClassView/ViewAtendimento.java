    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        SolicitacaoOSBeans sol = new SolicitacaoOSBeans();
        AtendimentoDao aten = new AtendimentoDao();
        AtendimentoDao at = new AtendimentoDao();
        if(jTextInfAt.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Para continuar e necessário preencher os campos");
        }
        else if(jTableOsAbertas.getSelectedRow() != -1){            
            sol.setSts_id((StatusBeans)jComboStatus.getSelectedItem());
            sol.setTec_id((TecnicosBeans) jComboTecnicos.getSelectedItem());
            sol.setInf_atendimento(jTextInfAt.getText());
            sol.setId_solicitacao((int) jTableOsAbertas.getValueAt(jTableOsAbertas.getSelectedRow(),0));
            aten.UpdateSolicitacao(sol);
            
            ReadTable();
            
            jTextCodOs.setText("");
            jTextSolicitante.setText("");
            jTextDepartamento.setText("");
            jTextPrioridade.setText("");
            jTextCabecalho.setText("");
            jTextAssunto.setText("");
            jTextdata.setText("");
            jTextInfAt.setText("");
            jComboStatus.setSelectedItem(null);
            jComboTecnicos.setSelectedItem(null);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

