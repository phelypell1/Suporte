     public void ReadTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTableOsAbertas.getModel();
        NovaOSDao OsAberta = new NovaOSDao();
        modelo.setNumRows(0);
        for (SolicitacaoOSBeans sol : OsAberta.ReadTable()) {
            modelo.addRow(new Object[]{
                sol.getId_solicitacao(),
                sol.getUser_id(),
                sol.getDep_id(),
                sol.getInfo_cab(),
                sol.getInfo_sol(),
                sol.getData_sol(),
                sol.getUrg_id(),
                sol.getSts_id(),});
        }
    }

