    private void btnConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirActionPerformed
        if(jtableProdutos.getSelectedRow() != -1){
            MaterialBeans mat = new MaterialBeans();
            MaterialDao mate = new MaterialDao();
            
            mat.setNome_material(textEdProd.getText());
            mat.setData_entrada(jDateEdiAlterar.getDateEditor().getUiComponent().getName());
            mat.setQuantidade(Integer.parseInt(textEdiQuant.getText()));
            mat.setUser_id((UsuariosBeans) jComboEdiUsuario.getSelectedItem());
            mat.setObser(textObser.getText());
            mat.setValor_unit(Double.parseDouble(jtextEdiValor.getText()));
            mat.setId_material((int) jtableProdutos.getValueAt(jtableProdutos.getSelectedRow(),0));
            mate.btnAlterar(mat);
            
            textEdProd.setText("");
            jDateEdiAlterar.setDate(null);
            textEdiQuant.setText("");
            jComboEdiUsuario.setSelectedItem(null);
            textObser.setText("");
            jtextEdiValor.setText("");
                    
            ReadTable();
        }
        
    }//GEN-LAST:event_btnConcluirActionPerformed

