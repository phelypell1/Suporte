    public void ReadTable(){
        DefaultTableModel table = (DefaultTableModel) jtableProdutos.getModel();
        MaterialDao mat = new MaterialDao();
        table.setNumRows(0);
        for(MaterialBeans mate : mat.MaterialRead()){
            table.addRow(new Object[]{
                mate.getId_material(),
                mate.getNome_material(),
                mate.getData_entrada(),
                mate.getQuantidade(),
                mate.getUser_id(),
                mate.getValor_unit(),
            });
        }
    }
